    public void saveCarData(String carPlate, String model, String availability, String price) throws IOException {
    // Path to your text file (replace "car_data.txt" with your desired filename)
    String filePath = "car_data.txt";
    
    System.out.println("Saving data to: " + filePath);

        // Build the data string with pipe (|) separators
        try ( // Create a FileWriter object to write to the file
            FileWriter writer = new FileWriter(filePath, true)) 
        {
            // Build the data string with pipe (|) separators
            String dataToSave = carPlate + "|" + model + "|" + availability + "|" + price + "\n";

            System.out.println("Data to save: " + dataToSave); // Print data before writing

            writer.write(dataToSave);
        }
        catch (IOException e) {
        // Handle potential IO exceptions during file operations
        JOptionPane.showMessageDialog(this, "An error occurred while saving data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }

    
    
