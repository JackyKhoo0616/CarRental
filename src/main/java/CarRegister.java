    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String carPlate = txtCarPlate.getText();
        String Model =  txtModel.getText();
        String Availability = ComAvl.getSelectedItem().toString();
        String Price = txtPrice.getText();
       
        
        // Check if any field is empty
        if (carPlate.isEmpty() || Model.isEmpty() || Availability.isEmpty() || Price.isEmpty()) {
          JOptionPane.showMessageDialog(this, "All fields are required to fill up!", "Error", JOptionPane.ERROR_MESSAGE);
          return; // Exit the method if there are empty fields
        }
        
        try {
            saveCarData(carPlate, Model, Availability, Price);
            
            JOptionPane.showMessageDialog(this, Model + " has successfully added!");
            txtCarPlate.setText("");
            txtModel.setText("");
            ComAvl.setSelectedIndex(-1);
            txtPrice.setText("");
            txtCarPlate.requestFocus();
        }
        catch(HeadlessException e){
            JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (IOException ex) {
            Logger.getLogger(CarRegister.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

